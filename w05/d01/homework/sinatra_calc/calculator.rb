  def add(num_1, num_2)
    num_1 + num_2
  end

  def subtract(num_1, num_2)
    num_1 - num_2
  end

  def multiply(num_1, num_2)
    num_1 * num_2
  end

  def divide(num_1, num_2)
    num_1 / num_2
  end

  def root(num_1, num_2)
    num_1 ** (1 / num_2)
  end

  def power(num_1, num_2)
    num_1 ** num_2
  end
end
